package Models;

public class PPEModel {
    private int id;
	private String name;
	private int quantity;
	
	public int getID() 
	{
		return id;
	}
	
	public void setID(int ID) 
	{
		this.id = ID;
	}
	
	
	public String getNAME() 
	{
		return name;
	}
	
	public void setNAME(String NAME) 
	{
		this.name = NAME;
	}
	
	public int getQUANTITY() 
	{
		return quantity;
	}
	
	public void setQUANTITY(int QUANTITY) 
	{
		this.quantity = QUANTITY;
	}
}
